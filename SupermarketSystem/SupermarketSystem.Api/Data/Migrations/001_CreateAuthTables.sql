CREATE TABLE IF NOT EXISTS Employees (
    Id BIGINT AUTO_INCREMENT PRIMARY KEY,
    FullName VARCHAR(150) NOT NULL,
    Username VARCHAR(50) NOT NULL UNIQUE,
    PasswordHash VARCHAR(255) NOT NULL,
    IsActive BOOLEAN NOT NULL DEFAULT TRUE,
    CreatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS AttendanceLog (
    Id BIGINT AUTO_INCREMENT PRIMARY KEY,
    EmployeeId BIGINT NOT NULL,
    LoginTime DATETIME NOT NULL,
    LogoutTime DATETIME NULL,
    CONSTRAINT FK_AttendanceLog_Employees FOREIGN KEY (EmployeeId) REFERENCES Employees(Id)
);

CREATE TABLE IF NOT EXISTS EmployeePermission (
    Id BIGINT AUTO_INCREMENT PRIMARY KEY,
    EmployeeId BIGINT NOT NULL,
    PermissionKey VARCHAR(50) NOT NULL,
    CONSTRAINT FK_EmployeePermission_Employees FOREIGN KEY (EmployeeId) REFERENCES Employees(Id)
);
